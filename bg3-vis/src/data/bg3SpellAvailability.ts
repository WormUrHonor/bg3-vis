const invocationSpellMap: Partial<Record<WarlockInvocation, string[]>> = {
  "Beguiling Influence": [],
  "Armour of Shadows": ["invocation-mage-armour"],
  "Beast Speech": ["invocation-speak-with-animals"],
};

export function isSpellAvailableForBuild(
  spell: BG3Spell,
  selectedClass: ClassName | "",
  selectedSubclass: string,
  selectedLevel: number,
  selectedWarlockInvocations: WarlockInvocation[] = []
): boolean {
  if (!selectedClass) return false;

  if (spell.id.startsWith("invocation-")) {
    return selectedWarlockInvocations.some((invocation) =>
      invocationSpellMap[invocation]?.includes(spell.id)
    );
  }

  const sources = spellAvailability[spell.id] ?? [];

  return sources.some((spellSource) => {
    const classMatches = spellSource.className === selectedClass;
    const levelMatches = selectedLevel >= spellSource.minLevel;
    const subclassMatches = !spellSource.subclass || spellSource.subclass === selectedSubclass;

    return classMatches && levelMatches && subclassMatches;
  });
}

export function getAvailableSpellsForBuild(
  spells: BG3Spell[],
  selectedClass: ClassName | "",
  selectedSubclass: string,
  selectedLevel: number,
  selectedWarlockInvocations: WarlockInvocation[] = []
): BG3Spell[] {
  return spells.filter((spell) =>
    isSpellAvailableForBuild(
      spell,
      selectedClass,
      selectedSubclass,
      selectedLevel,
      selectedWarlockInvocations
    )
  );
}