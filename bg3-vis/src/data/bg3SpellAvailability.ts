type SpellSource = {
  className: ClassName;
  minLevel: number;
  subclass?: string;
};

const fullCasterLevelByRank: Record<number, number> = {
  1: 1,
  2: 3,
  3: 5,
  4: 7,
  5: 9,
  6: 11,
};

const halfCasterLevelByRank: Record<number, number> = {
  1: 2,
  2: 5,
  3: 9,
};

const thirdCasterLevelByRank: Record<number, number> = {
  1: 3,
  2: 8,
};

function source(className: ClassName, minLevel: number, subclass?: string): SpellSource {
  return { className, minLevel, subclass };
}

function full(className: ClassName, rank: number, subclass?: string): SpellSource {
  return source(className, fullCasterLevelByRank[rank], subclass);
}

function half(className: ClassName, rank: number, subclass?: string): SpellSource {
  return source(className, halfCasterLevelByRank[rank], subclass);
}

function third(className: ClassName, rank: number, subclass?: string): SpellSource {
  return source(className, thirdCasterLevelByRank[rank], subclass);
}

